      });
    },
    collect() {
      gsap.fromTo(
        ".ground-arrow",
        {
          duration: 1,
          ease: "power2.inOut",
          x: 1000,
          opacity: 0,
        },
        {
          x: 0,
          opacity: 0.3,
        }
      );
    },
  },

  setup() {
    const beforeEnter = (el) => {
      gsap.set(el, {
        opacity: 0,
        y: -100,
      });
    };
    const enter = (el, done) => {
      gsap.to(el, {
        duration: 1,
        opacity: 1,
        y: 0,
        onComplete: done,
        ease: "bounce.out",
      });
    };

    return {
      beforeEnter,
      enter,
    };
  },
};
